public class Switch{
    public static void main(String args[]){
        int b=10;
        switch(b){
          case 1: System.out.println("First Case");
                  break;
          case 2: System.out.println("Second Case");
                  break;
          case 3: System.out.println("Third Case");
                  break;
          default:System.out.println("Default Case");
                       
        }
    }
}